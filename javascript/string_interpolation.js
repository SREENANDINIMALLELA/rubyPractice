//  we can insert, or interpolate, variables into strings using template literals
const myPet = 'armadillo';
console.log(`I own a pet ${myPet}.`);
// Output: I own a pet armadillo.
// here ${}is the templet literals Inside the template literal,
//  The value of myPet is inserted into the template literal.
//a template literal is wrapped by backticks (` code in side`)
